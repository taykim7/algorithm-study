function solution(arr1, arr2) {
  let arr = [];

  // Two Pointers
  let p1 = 0;
  let p2 = 0;

  // λ©”μΈ λ³‘ν•© λ£¨ν”„
  while (p1 < arr1.length && p2 < arr2.length) {
    if (arr1[p1] < arr2[p2]) {
      arr.push(arr1[p1++]);
    } else {
      arr.push(arr2[p2++]);
    }
  }

  // ν•μ½ λ°°μ—΄μ΄ λ¨Όμ € λλ‚¬μ„ κ²½μ° λ‚¨μ€ κ°’ κ·Έλ€λ΅ push
  while (p1 < arr1.length) {
    arr.push(arr1[p1++]);
  }
  while (p2 < arr2.length) {
    arr.push(arr2[p2++]);
  }

  return arr;
}

