function solution(arr, num) {
  
  let tot = 0;
  let count = 0;

  for (let i = 0; i < arr.length; i++) {
    tot = 0;
    for (let j = i; j < arr.length; j++) {
      tot += arr[j]
      if (tot > num) continue;
      else if (tot === num) {
        count += 1;
        continue;
      }
    }
  }
  return count;
}
