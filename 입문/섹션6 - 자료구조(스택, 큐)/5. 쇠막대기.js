function solution(str) {
  let stack = [];
  let result = 0;
  for (let i = 0; i < str.length; i++) {
    if (str[i] === '(') {
      stack.push(str[i]);
    } else {
      stack.pop();
      if (str[i - 1] === '(') {
        result += stack.length;
      } else {
        result += 1;
      }
    }
  }
  return result;
}

