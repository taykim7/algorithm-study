function solution(str) {
  let count = 0;
  let stack = [];
  for (let i = 0; i < str.length; i++) {
    if (str[i] === '(') {
      stack.push(1);
    } else {
      stack.pop();
      if (str[i - 1] === '(') {
        // 레이져
        count += stack.length
      } else {
        count++;
      }
    }
  }
  return count;
}
